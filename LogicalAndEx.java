package operators;

public class LogicalAndEx {

	public static void main(String[] args) {
		  int x = 5;
	        int y = 10;
	        
	        // Using logical AND operator to check if both x and y are greater than 0
	        if (x > 0 && y > 0) 
	        {
	            System.out.println("Both x and y are greater than 0");
	        } 
	        else 
	        {
	            System.out.println(" not greater than 0");
	        }
}
}
